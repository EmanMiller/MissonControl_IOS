#!/usr/bin/env node
/**
 * Generates iOS App Icon PNGs from SVGs in AppIcon.appiconset.
 * Run: node scripts/generate-app-icons.js
 */
const fs = require('fs');
const path = require('path');

const sharp = require('sharp');

const APPICON_DIR = path.join(
  __dirname,
  '..',
  'ios/MissionControlMobile/Images.xcassets/AppIcon.appiconset'
);

const PAIRS = [
  ['icon-20@2x.svg', 'icon-20@2x.png'],
  ['icon-20@3x.svg', 'icon-20@3x.png'],
  ['icon-29@2x.svg', 'icon-29@2x.png'],
  ['icon-29@3x.svg', 'icon-29@3x.png'],
  ['icon-40@2x.svg', 'icon-40@2x.png'],
  ['icon-40@3x.svg', 'icon-40@3x.png'],
  ['icon-60@2x.svg', 'icon-60@2x.png'],
  ['icon-60@3x.svg', 'icon-60@3x.png'],
  ['icon-1024.svg', 'icon-1024.png'],
];

// iPad sizes: generated by resizing 1024 source
const RESIZE_FROM_1024 = [
  { filename: 'icon-76@2x.png', size: 152 },
  { filename: 'icon-83.5@2x.png', size: 167 },
];

async function main() {
  for (const [svgName, pngName] of PAIRS) {
    const svgPath = path.join(APPICON_DIR, svgName);
    const outPath = path.join(APPICON_DIR, pngName);
    if (!fs.existsSync(svgPath)) {
      console.warn('Skip (no SVG):', svgName);
      continue;
    }
    const buf = fs.readFileSync(svgPath);
    await sharp(buf).png().toFile(outPath);
    console.log('Generated', pngName);
  }
  const src1024 = path.join(APPICON_DIR, 'icon-1024.png');
  if (fs.existsSync(src1024)) {
    for (const { filename, size } of RESIZE_FROM_1024) {
      const outPath = path.join(APPICON_DIR, filename);
      await sharp(src1024).resize(size, size).png().toFile(outPath);
      console.log('Generated', filename);
    }
  }
  console.log('Done.');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
